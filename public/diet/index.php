<h2 class="blue">Eating away at Alzheimer's risk</h2>

The MIND diet was developed by researchers at Chicago’s Rush University Medical Center to lower one's risk of developing Alzheimer's disease by up to 53%. Using this diet, Brainsalvation has created an innovative diet program, designed specifically for women. Members work closely with expert nutrition consultants and health coaches to achieve their personal goals. They receive daily evaluation of dietary choices, customized nutrition recommendations, guidance, and support. 
The MIND diet is a hybrid of the Mediterranean and DASH (Dietary Approaches to Stop Hypertension) diets, but with modifications based on the most compelling science on nutrition and dementia prevention. Both the DASH and Mediterranean diets have been found to reduce the risk of <strong>cardiovascular conditions like hypertension, heart attack, and stroke.</strong>

